/**
 * Wrapper for themes.
 *  
 * @author Christian Melchior <christian@ilios.dk>
 */
public class StyleTemplate {
	
	public StyleTemplate(int top, int bottom) {
		this.top = top;
		this.bottom = bottom;
	}
	
	public int top;
	public int bottom;
}
